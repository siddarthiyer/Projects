; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

;This program is used to print a null terminated string on the monitor in a large font.
;The font data for each specific ascii character is stored in 16 consecutive memory locations with the leading
;8 bits of each memory location  used. The string to be printed is stored in consecutive memory locations
; with the starting character stored at x5002.
;The characters used to print the characters of the string are stored in x5000 and x5001
;(Char in x5000 printed if bit in address is 0 and char in x5001 is printed if bit in address is 1)

;This program primarily uses registers in the following manner
;R0 first contains the address of the ascii character to be printed then the hex value of the ascii character
;R1 contains the address of the hex value of the character being printed
;R2 contains the address of the font data for the row currently being printed
;R3 is used as a row counter
;R4 is used as a column counter
;R5 is used to store the data of the current row. (Data stored in address stored in R2)
;R6 is used to keep track of which address line of the character needs to be printed. (Keeps track of row)


		.ORIG x3000		;code begins at memory location x3000

		
		AND 	R3, R3, #0	
		ADD 	R3, R3, #8		
		ADD 	R3, R3, #8	;Row counter set to 16
		AND	R6, R6, #0	;Counter for address line

NEXT_ROW 	LD 	R1, ASC_LOC	;Load R1 with address x5002	
		ADD 	R3, R3, #0	;Sets CC to check if Row counter is 0
		BRz 	DONE		;Branch to DONE if row counter is 0		
		

NEXT_LETTER	AND 	R4, R4, #0
		ADD 	R4, R4, #8	;Set Column Counter to 8
		LDR 	R2, R1, #0	;Load R2 with hex value stored in memory location in R1		
	  	
		ADD	R2, R2, #0	;Set CC to check if hex value is null
		BRz 	DONE_ROW	;Branch to DONE_ROW if hex value is null

		ADD	R2, R2, R2	
		ADD	R2, R2, R2
		ADD	R2, R2, R2
		ADD	R2, R2, R2	;Multiply the hex value in R2 by 16
		LEA	R5, FONT_DATA	;Store starting address of the font data in R5
		ADD	R2, R2, R5	;Add R2 and R5 to get address of font data of hex value
		ADD	R2, R2, R6	;Ensure the address corresponds to the current row being printed 
		ADD	R1, R1, #1	;Increment R1 to store address of the next character
		LDR 	R5, R2, #0	;Load R5 with data stored in memory location for that row(R2)
		

NEXT_COLUMN	ADD 	R4, R4, #0	;Set CC to check if Column Counter is 0
		BRz 	NEXT_LETTER	;Branch to NEXT_LETTER if column counter is 0
		LD 	R0, CHAR1	;Load R0 with memory location x5000
		ADD	R5, R5, #0	;Set CC to check whether R5 is negative(1 in MSB)
		BRzp 	#1		;Skip one instruction if R5 is zero or positive
		ADD 	R0, R0, #1	;Increment R0(Stores x5001 if MSB is 1.Instruction skipped if MSB is 0)
		LDR 	R0, R0, #0	;R0 is loaded with data stored in Memory Location stored in R0
		OUT			;Character is print on the screen
		ADD	R5, R5, R5	;Bits of R5 is left-shifted once
		ADD 	R4, R4, #-1	;Column counter is decremented
		BRnzp 	NEXT_COLUMN	;Branch to NEXT_COLUMN

DONE_ROW	LD 	R0, ASCII_NL
		OUT			;Go to next line when row is finished
		ADD 	R3, R3, #-1	;Decrement row counter
		ADD 	R6, R6, #1	;Increment R6 to ensure data of the next row is printed
		BRnzp 	NEXT_ROW	;Branch to NEXT_ROW

DONE		HALT			;Halt the program
ASCII_NL	.FILL xA		;Stores ascii character for next line	
ASC_LOC		.FILL	x5002		;Stores memory address of the first character of the string
CHAR1		.FILL	x5000		;Stores address of character to be printed when for bit 0 in font data 
